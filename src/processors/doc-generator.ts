/**
 * Doc Generator
 * Converts aggregated data + AI output into Markdown pages for VitePress
 */

const DOC_SYSTEM_PROMPT = `You are a technical documentation writer. Write clear, well-structured Markdown documentation.

Rules:
- Use proper Markdown formatting (headers, lists, tables, code blocks)
- Be concise but comprehensive
- Use professional tone
- Include relevant details from the provided context
- Do NOT add markdown frontmatter (---) unless instructed`;

/**
 * Generate all documentation pages
 */
export async function generateAllDocs(
  data: AggregatedData,
  specs: TechnicalSpec[],
  diagrams: DiagramOutput[],
  outputDir: string
): Promise<GeneratedPage[]> {
  const pages: GeneratedPage[] = [];
  const spinner = logger.spinner('Generating documentation pages...');

  try {
    // 1. Project Overview
    spinner.text = 'Generating project overview...';
    pages.push(await generateProjectOverview(data, specs, outputDir));

    // 2. Architecture page with diagrams
    spinner.text = 'Generating architecture documentation...';
    pages.push(generateArchitecturePage(diagrams, outputDir));

    // 3. Story pages (grouped by epic)
    if (data.jira) {
      spinner.text = 'Generating story documentation...';
      const storyPages = await generateStoryPages(data, specs, diagrams, outputDir);
      pages.push(...storyPages);
    }

    // 4. Module pages
    spinner.text = 'Generating module documentation...';
    const modulePages = await generateModulePages(data, diagrams, outputDir);
    pages.push(...modulePages);

    // 5. Generate VitePress config
    spinner.text = 'Generating VitePress configuration...';
    generateVitePressConfig(pages, data, outputDir);

    // 6. Update homepage
    spinner.text = 'Updating homepage...';
    generateHomepage(data, specs, outputDir);

    spinner.succeed(`Generated ${pages.length} documentation pages`);
    return pages;
  } catch (err) {
    spinner.fail('Documentation generation failed');
    throw err;
  }
}

/**
 * Generate the project overview page
 */
async function generateProjectOverview(
  data: AggregatedData,
  specs: TechnicalSpec[],
  outputDir: string
): Promise<GeneratedPage> {
  const storyCount = data.jira?.stories.length || 0;
  const epicCount = data.jira?.epics.length || 0;

  const context = `
Project: ${data.project.name}
Description: ${data.project.description || 'N/A'}
Type: ${data.local?.type || 'Unknown'}
Dependencies: ${data.local ? Object.keys(data.local.dependencies).slice(0, 20).join(', ') : 'N/A'}
User Stories: ${storyCount}
Epics: ${epicCount}
Source Files: ${data.local?.sourceFiles.length || 'N/A'}
Directories: ${data.local?.directories.slice(0, 15).join(', ') || 'N/A'}
`;

  const overview = await generateContent(
    `Write a comprehensive project overview documentation page based on this information:

${context}

${data.github?.structure.readme ? `## Project README:\n${data.github.structure.readme.substring(0, 3000)}` : ''}

Include these sections:
1. **Overview** — What the project does
2. **Tech Stack** — Technologies used (from dependencies)
3. **Project Structure** — Key directories and their purpose
4. **Getting Started** — How to set up the project (based on scripts)
5. **Key Statistics** — Stories, modules, etc.`,
    {
      systemInstruction: DOC_SYSTEM_PROMPT,
      temperature: 0.4,
      maxTokens: 4096,
    }
  );

  const content = `---
title: Project Overview
description: AI-generated overview of ${data.project.name}
---

${overview}

---

> 📝 *This documentation was auto-generated by [mvdoc](https://github.com/mvdoc) on ${new Date().toLocaleDateString()}*
`;

  const filePath = path.join(outputDir, 'overview.md');
  writeFile(filePath, content);

  return {
    filePath,
    title: 'Project Overview',
    content,
    category: 'overview',
  };
}

