// ─── Helpers ───

function groupFilesByModule(files: SourceFile[]): Map<string, SourceFile[]> {
  const groups = new Map<string, SourceFile[]>();

  for (const file of files) {
    const parts = file.relativePath.split('/');
    // Use the first directory as module name, or 'root' for top-level files
    const moduleName = parts.length > 1 ? parts[0] : 'root';

    if (!groups.has(moduleName)) {
      groups.set(moduleName, []);
    }
    groups.get(moduleName)!.push(file);
  }

  return groups;
}

function buildSidebar(pages: GeneratedPage[], outputDir: string): SidebarItem[] {
  const sidebar: SidebarItem[] = [];

  // Overview
  sidebar.push({ text: 'Project Overview', link: '/overview' });

  // Architecture
  const archPages = pages.filter((p) => p.category === 'architecture');
  if (archPages.length > 0) {
    sidebar.push({
      text: 'Architecture',
      link: '/architecture/',
      collapsed: false,
    });
  }

  // Stories
  const storyPages = pages.filter((p) => p.category === 'story');
  if (storyPages.length > 0) {
    sidebar.push({
      text: 'User Stories',
      link: '/stories/',
      collapsed: true,
      items: storyPages
        .filter((p) => !p.filePath.endsWith('index.md'))
        .slice(0, 30) // Limit sidebar items
        .map((p) => ({
          text: p.title,
          link: '/' + path.relative(outputDir, p.filePath).replace(/\.md$/, ''),
        })),
    });
  }

  // Modules
  const modulePages = pages.filter((p) => p.category === 'module');
  if (modulePages.length > 0) {
    sidebar.push({
      text: 'Modules',
      link: '/modules/',
      collapsed: true,
      items: modulePages
        .filter((p) => !p.filePath.endsWith('index.md'))
        .map((p) => ({
          text: p.title,
          link: '/' + path.relative(outputDir, p.filePath).replace(/\.md$/, ''),
        })),
    });
  }

  return sidebar;
}

function slugify(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}
